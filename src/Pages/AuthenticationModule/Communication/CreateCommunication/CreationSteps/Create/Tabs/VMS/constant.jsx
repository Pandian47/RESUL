export const getTestType = (type) => {
    if (type === 'form' || type === 'save') return 0;
    else if (type === 'request for approval') return 1;
    else if (type === 'test preview') return 2;
    else if (type === 'live') return 4;
    return 0;
};

export const buildPayload = (formState, type, location) => {
    try {
    let {
        audience,
        schedule,
        timezone,
        userId,
        clientId,
        departmentId,
        campaignId,
        campaignType,
        dataSource = 'TL',
        levelNumber = 1,
        dynamiclistId = 0,
        domId = '',
        addOnLevel = 1,
        isALLorAny = 'ALL',
        parentChannelDetailId = 0,
        parentChannelDetailType = 'VMS',
        actionId = 1,
        actionTime = 1,
        activeChannel = 21,
        actionTimeDuration = 'D',
        channelFriendlyName = '',
        channelDetailType = 'VMS',
        channelId = 'ch0025',
        flowChannel = 25,
        channelDetailId,
        provider,
        templateName,
        language,
        editorText,
        repeat,
        content,
        audioPath,
        voiceTestNumber,
        dialCode,
        fileWeight = '',
        mobile,
        currentTab,
        daylightSavings,
    } = ensureObject(formState);

    const totalAudience = sumAudienceCountByField(audience, 'recipientCountEmail');
    const { timeZoneId = 0 } = getUserDetails();
    let isSendTestVMS = getTestType(type);

    schedule = resolveMdcSchedule(formState, location, levelNumber, campaignType, dataSource, schedule);

    return {
        departmentId,
        clientId,
        copy: false,
        createdBy: userId,
        userId,
        segmentationListId: _map(ensureArray(audience), 'segmentationListId'),
        dataSource: campaignType === 'T' ? 'DL' : dataSource,
        vmsCampaign: {
            campaignId,
            campaignType,
            totalAudience,
            levelNumber,
            fileWeight: fileWeight || '', // size
            isSendTimeOptEnable: false,
            dynamiclistId,
            domId,
            addOnLevel,
            isALLorAny,
            parentChannelDetailId,
            parentChannelDetailType,
            actionTime,
            activeChannel,
            actionTimeDuration,
            channelFriendlyName,
            isSplitAbEnabled: false,
            channelDetailType,
            channelId,
            flowChannel,
            senderId: _get(provider, 'clientVMSSettingId', 0),
            testVMSMobileNo: isSendTestVMS === 2 ? mobile.slice(dialCode?.length + 1) : '',
            countryCodeMobile: dialCode?.length && dialCode.includes('+') ? dialCode.split('+')[1] : dialCode || '',
            isSendTestVMS,
            actionId,
            content: [
                {
                    vmsTemplateId: _get(templateName, 'vmsTemplateId', 0) || 0,
                    retryCount: currentTab === 0 ? repeat : '', //Txt
                    voiceRepeatCount: currentTab === 1 ? repeat : '', //Voice
                    //vmsChannelDetailId: _get(content?.[0], 'vmsChannelDetailId', 0),
                    vmsChannelDetailId: channelDetailId || 0,
                    audioFilePath: audioPath || '',
                    templateContent: editorText || '',
                    languageId: _get(language, 'vMSLanguageId', 0) || 0,
                    timeZoneId: handleAllChannelTimeZonePayload(
                        campaignType,
                        location?.timeZoneId,
                        timezone,
                        timeZoneId,
                        location
                    ),
                    splitType: null,
                    localBlastDateTime: resolveLocalBlastDateTime({
                        campaignType,
                        statusId: _get(content?.[0], 'statusId', 0),
                        triggerPlayPauseStatus: formState?.triggerPlayPauseStatus,
                        schedule,
                    }),
                    isDaylightSavings: daylightSavings || false,
                },
            ],
            requestForApproval: {
                isWorkflowEnabled: false,
                approvarList: [],
            },
            ...handleMDCExtraPayload(location),
            ...handleAllChannelPayload('vms', formState)
        },
    };
    } catch {
        return {
            departmentId: 0,
            clientId: 0,
            copy: false,
            createdBy: 0,
            userId: 0,
            segmentationListId: [],
            dataSource: 'TL',
            vmsCampaign: {},
        };
    }
};

